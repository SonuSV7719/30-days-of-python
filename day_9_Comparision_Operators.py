# Comparision Operators

# ==, >, <, <=, >=, !=

# Uses 
# --> to check Coditions
# --> Used in if else
# --> Used in loops

# Ouput of comparision operator 
# is always --> true or false

# ==

num1 = 2
num2 = 3
str1 = "Sonu"
str2 = "Sonu"
str3 = "sonu"
print(num1 == num2) # ouput --> False

print(str2 == str3) # output --> False
print(str2 == str1) # ouput --> True


# > and < 

print(2 < 3) # output --> True
print(2 > 3) # output --> False

# <= and >= 

print(2 <= 2) # output --> True
print(2 <= 4) # output --> True
print(2 >= 1) # ouput --> True


# != 
print(2 != 2) # Ouput --> False